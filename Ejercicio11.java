 class Ejercicio11{
    public static void main(String[] args) {
        int numero = 608;
        int i = 0;

        // Imprimir los números pares del 0 al número dado usando while
        while (i <= numero) {
            System.out.println(i);
            i += 2; // Incrementa en 2 para obtener solo los números pares
        }
    }
}
